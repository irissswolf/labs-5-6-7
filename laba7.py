print("3 задание")
days_of_week = ("Понедельник", "Вторник", "Среда", "Четверг", "Пятница", "Суббота", "Воскресенье")
weekends_count = int(input("Сколько выходных дней на неделе вы хотите? "))

weekends = days_of_week[-weekends_count:] #отрицательные индексы считают с конца
workdays = days_of_week[:-weekends_count] # :-weekends_count-с начала и до позиции -weekends_count (не включая её)

print("Ваши выходные дни:", ", ".join(weekends)) # join() соединяет слова в строку
print("Ваши рабочие дни:", ", ".join(workdays))

