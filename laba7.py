print("2 задание")
список = [3, 7, 2, 7, 5, 9, 2, 1, 8, 3]
print("список:", список)
просмотренные = set() # множество для просмотренных элементов
повторы = set() #множество для повторяющихся элементов
#Проходим по каждому элементу списка
for элемент in список:
    if элемент in просмотренные:
            повторы.add(элемент)  #add-добавляем элемент
    else:
            просмотренные.add(элемент)
if повторы:
        print("Найдены повторяющиеся элементы:", повторы)
        for элемент in повторы:
            print(f"Элемент {элемент} повторяется в списке")
else:
        print("Повторяющихся элементов нет")


